def 仕訳バリデーション(仕訳一覧, マスタ=None, 会計期間=None):
    """
    仕訳エントリのリストを検証し、問題があれば警告リストを返す。

    引数:
        仕訳一覧: list[仕訳エントリ]
        マスタ: マスタデータ（科目コード検証用、省略可）
        会計期間: (開始YYYYMMDD, 終了YYYYMMDD) のタプル（省略可）

    戻り値:
        list[dict]: 警告リスト [{"種別": "...", "内容": "...", "行番号": N}, ...]
    """
    警告一覧 = []

    for i, エントリ in enumerate(仕訳一覧, 1):
        # 1. 貸借一致チェック
        if エントリ.借方金額 != エントリ.貸方金額:
            if エントリ.借方科目コード != "997" and エントリ.貸方科目コード != "997":
                警告一覧.append({
                    "種別": "貸借不一致",
                    "内容": f"借方{エントリ.借方金額:,} != 貸方{エントリ.貸方金額:,}",
                    "行番号": i,
                })

        # 2. 科目コード存在チェック
        if マスタ:
            for 側, コード in [("借方", エントリ.借方科目コード), ("貸方", エントリ.貸方科目コード)]:
                if コード and コード != "997" and not マスタ.科目名取得(コード):
                    警告一覧.append({
                        "種別": "科目コード不在",
                        "内容": f"{側}科目コード {コード} がマスタに存在しません",
                        "行番号": i,
                    })

        # 3. 日付範囲チェック
        if 会計期間 and エントリ.日付:
            開始, 終了 = 会計期間
            if エントリ.日付 < 開始 or エントリ.日付 > 終了:
                警告一覧.append({
                    "種別": "期間外",
                    "内容": f"日付 {エントリ.日付} が会計期間 {開始}〜{終了} の範囲外",
                    "行番号": i,
                })

        # 4. 金額ゼロチェック
        if エントリ.借方金額 == 0 and エントリ.貸方金額 == 0:
            警告一覧.append({
                "種別": "金額ゼロ",
                "内容": "借方・貸方ともに金額が0",
                "行番号": i,
            })

    # 5. 重複検出（同日・同額・同摘要）
    seen = {}
    for i, エントリ in enumerate(仕訳一覧, 1):
        キー = f"{エントリ.日付}|{エントリ.借方金額}|{エントリ.摘要}"
        if キー in seen:
            警告一覧.append({
                "種別": "重複の可能性",
                "内容": f"行{seen[キー]}と同一（{エントリ.日付} {エントリ.借方金額:,}円 {エントリ.摘要}）",
                "行番号": i,
            })
        else:
            seen[キー] = i

    return 警告一覧
