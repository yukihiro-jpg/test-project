"""
会計大将インポートCSV出力モジュール
26列フォーマットのCSVファイルを生成する
"""
import csv
import os
from datetime import datetime


class 仕訳エントリ:
    """仕訳1行分のデータ"""

    def __init__(self):
        self.日付 = ""                # YYYYMMDD
        self.借方科目コード = ""
        self.借方科目名 = ""
        self.借方補助コード = ""
        self.借方補助名 = ""
        self.借方税売仕 = "0"         # 0=対象外, 1=売上, 2=仕入
        self.借方業種 = "0"
        self.借方税込抜 = "0"         # 0=税込, 1=税抜
        self.借方金額 = 0
        self.借方消費税額 = 0
        self.借方税コード = ""
        self.借方税率コード = ""
        self.借方事業者区分 = "0"
        self.貸方科目コード = ""
        self.貸方科目名 = ""
        self.貸方補助コード = ""
        self.貸方補助名 = ""
        self.貸方税売仕 = "0"
        self.貸方業種 = "0"
        self.貸方税込抜 = "0"
        self.貸方金額 = 0
        self.貸方消費税額 = 0
        self.貸方税コード = ""
        self.貸方税率コード = ""
        self.貸方事業者区分 = "0"
        self.摘要 = ""
        self.信頼度 = ""              # 出力には含まない。デバッグ用

    def 行データ変換(self):
        """26列のリストに変換"""
        return [
            self.日付,
            self.借方科目コード, self.借方科目名,
            self.借方補助コード, self.借方補助名,
            self.借方税売仕, self.借方業種, self.借方税込抜,
            str(self.借方金額), str(self.借方消費税額),
            self.借方税コード, self.借方税率コード, self.借方事業者区分,
            self.貸方科目コード, self.貸方科目名,
            self.貸方補助コード, self.貸方補助名,
            self.貸方税売仕, self.貸方業種, self.貸方税込抜,
            str(self.貸方金額), str(self.貸方消費税額),
            self.貸方税コード, self.貸方税率コード, self.貸方事業者区分,
            self.摘要,
        ]

    @classmethod
    def 通帳取引から生成(cls, 取引, 借方情報, 貸方情報, 摘要=""):
        """通帳取引から仕訳エントリを生成"""
        エントリ = cls()
        エントリ.日付 = 取引.日付
        エントリ.摘要 = 摘要 or 取引.摘要

        if 取引.出金額 > 0:
            # 出金: 借方=費用等、貸方=銀行口座
            エントリ.借方金額 = 取引.出金額
            エントリ.貸方金額 = 取引.出金額
            エントリ.借方科目コード = 借方情報.get("科目コード", "174")
            エントリ.借方科目名 = 借方情報.get("科目名", "仮払金")
            エントリ.借方補助コード = 借方情報.get("補助コード", "")
            エントリ.借方補助名 = 借方情報.get("補助名", "")
            エントリ.貸方科目コード = 貸方情報.get("科目コード", "")
            エントリ.貸方科目名 = 貸方情報.get("科目名", "")
            エントリ.貸方補助コード = 貸方情報.get("補助コード", "")
            エントリ.貸方補助名 = 貸方情報.get("補助名", "")
        else:
            # 入金: 借方=銀行口座、貸方=売上等
            エントリ.借方金額 = 取引.入金額
            エントリ.貸方金額 = 取引.入金額
            エントリ.借方科目コード = 借方情報.get("科目コード", "")
            エントリ.借方科目名 = 借方情報.get("科目名", "")
            エントリ.借方補助コード = 借方情報.get("補助コード", "")
            エントリ.借方補助名 = 借方情報.get("補助名", "")
            エントリ.貸方科目コード = 貸方情報.get("科目コード", "174")
            エントリ.貸方科目名 = 貸方情報.get("科目名", "仮払金")
            エントリ.貸方補助コード = 貸方情報.get("補助コード", "")
            エントリ.貸方補助名 = 貸方情報.get("補助名", "")

        # 消費税設定
        税借方 = 借方情報.get("消費税", {})
        if 税借方:
            エントリ.借方税売仕 = 税借方.get("ts", "0")
            エントリ.借方業種 = 税借方.get("gy", "0")
            エントリ.借方税込抜 = 税借方.get("tt", "0")
            エントリ.借方税コード = 税借方.get("tc", "")
            エントリ.借方税率コード = 税借方.get("tr", "")
            エントリ.借方事業者区分 = 税借方.get("bt", "0")

        税貸方 = 貸方情報.get("消費税", {})
        if 税貸方:
            エントリ.貸方税売仕 = 税貸方.get("ts", "0")
            エントリ.貸方業種 = 税貸方.get("gy", "0")
            エントリ.貸方税込抜 = 税貸方.get("tt", "0")
            エントリ.貸方税コード = 税貸方.get("tc", "")
            エントリ.貸方税率コード = 税貸方.get("tr", "")
            エントリ.貸方事業者区分 = 税貸方.get("bt", "0")

        return エントリ


def CSV出力(仕訳一覧, 出力パス, 文字コード="cp932"):
    """
    仕訳エントリのリストをCSVファイルに出力

    引数:
        仕訳一覧: list[仕訳エントリ]
        出力パス: 出力ファイルパス
        文字コード: エンコーディング（デフォルトはShift_JIS互換のcp932）
    """
    os.makedirs(os.path.dirname(出力パス), exist_ok=True)

    with open(出力パス, "w", encoding=文字コード, newline="") as f:
        writer = csv.writer(f)
        for エントリ in 仕訳一覧:
            writer.writerow(エントリ.行データ変換())

    return 出力パス


def レビュー用CSV出力(仕訳一覧, 要確認一覧, 出力パス):
    """
    要確認リスト付きCSVを出力（人間レビュー用）

    引数:
        仕訳一覧: 確定仕訳のリスト
        要確認一覧: 要確認の取引リスト
        出力パス: 出力パス
    """
    os.makedirs(os.path.dirname(出力パス), exist_ok=True)

    with open(出力パス, "w", encoding="utf-8-sig", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["=== 要確認取引 ===", "", "", "", ""])
        writer.writerow(["日付", "摘要", "金額", "出入", "理由"])
        for 項目 in 要確認一覧:
            writer.writerow([
                項目.get("日付", ""), 項目.get("摘要", ""),
                項目.get("金額", ""), 項目.get("方向", ""),
                項目.get("理由", ""),
            ])
        writer.writerow([])
        writer.writerow(["=== 確定仕訳 ==="] + [""] * 24)
        for エントリ in 仕訳一覧:
            writer.writerow(エントリ.行データ変換())

    return 出力パス
