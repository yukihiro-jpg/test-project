class 仕訳エンジン:
    """仕訳判定の中核エンジン"""

    def __init__(self, ルールブック: ルールブック管理, マスタ: マスタデータ, 顧問先パス=""):
        self.ルールブック = ルールブック
        self.マスタ = マスタ
        self.顧問先パス = 顧問先パス
        self.確定仕訳一覧 = []
        self.要確認一覧 = []

        # 売掛金管理
        self.売掛金 = 売掛金管理(顧問先パス) if 顧問先パス else None

        # 処理済み取引ログ（重複排除）
        self.処理済みログパス = os.path.join(顧問先パス, "processed_log.json") if 顧問先パス else ""
        self.処理済みログ = self._処理済みログ読み込み()
        self.処理統計 = {"スキップ": 0, "新規処理": 0}

        # 返済予定表
        self.返済予定表 = self._返済予定表読み込み(顧問先パス) if 顧問先パス else []

        # 士業報酬蓄積
        self.士業報酬一覧 = []

    # ============================================================
    # 処理済みログ（重複排除）
    # ============================================================

    def _フィンガープリント生成(self, 日付, 金額, 摘要, 口座コード, 方向):
        キー = f"{日付}|{金額}|{摘要}|{口座コード}|{方向}"
        return hashlib.md5(キー.encode("utf-8")).hexdigest()

    def _処理済みログ読み込み(self):
        if self.処理済みログパス and os.path.isfile(self.処理済みログパス):
            with open(self.処理済みログパス, "r", encoding="utf-8") as f:
                return json.load(f)
        return {}

    def _処理済みログ保存(self):
        if self.処理済みログパス:
            with open(self.処理済みログパス, "w", encoding="utf-8") as f:
                json.dump(self.処理済みログ, f, ensure_ascii=False, indent=2)

    def _処理済みチェック(self, 日付, 金額, 摘要, 口座コード, 方向):
        fp = self._フィンガープリント生成(日付, 金額, 摘要, 口座コード, 方向)
        return fp in self.処理済みログ

    def _処理済み登録(self, 日付, 金額, 摘要, 口座コード, 方向):
        fp = self._フィンガープリント生成(日付, 金額, 摘要, 口座コード, 方向)
        self.処理済みログ[fp] = {"日付": 日付, "金額": 金額, "摘要": 摘要, "口座": 口座コード}

    # ============================================================
    # 返済予定表
    # ============================================================

    def _返済予定表読み込み(self, 顧問先パス):
        予定表フォルダ = os.path.join(顧問先パス, "返済予定表")
        if not os.path.isdir(予定表フォルダ):
            return []
        import glob as glob_mod
        全予定 = []
        for CSVパス in glob_mod.glob(os.path.join(予定表フォルダ, "*.csv")):
            全予定.extend(self._返済予定表CSV解析(CSVパス))
        return 全予定

    def _返済予定表CSV解析(self, CSVパス):
        結果 = []
        全行 = []
        for 文字コード in ["cp932", "utf-8-sig", "utf-8"]:
            try:
                with open(CSVパス, "r", encoding=文字コード) as f:
                    全行 = list(csv_mod.reader(f))
                break
            except (UnicodeDecodeError, UnicodeError):
                continue
        if not 全行:
            return []

        ヘッダー行 = 全行[0]
        データ開始 = 1
        for i, 行 in enumerate(全行):
            行文字列 = "".join(str(c) for c in 行)
            if any(語 in 行文字列 for 語 in ["日付", "返済日", "引落日"]):
                ヘッダー行 = 行
                データ開始 = i + 1
                break

        列マップ = {}
        ヘッダー正規化 = [re.sub(r"\s+", "", str(c)) for c in ヘッダー行]
        for フィールド, キーワード一覧 in 返済予定表列キーワード.items():
            for kw in キーワード一覧:
                for j, h in enumerate(ヘッダー正規化):
                    if re.sub(r"\s+", "", kw) in h:
                        列マップ[フィールド] = j
                        break
                if フィールド in 列マップ:
                    break

        if "日付" not in 列マップ or "元本" not in 列マップ:
            return []

        for 行 in 全行[データ開始:]:
            if not 行 or len(行) <= max(列マップ.values()):
                continue
            日付セル = 行[列マップ["日付"]].strip()
            日付 = re.sub(r"[/\-年月]", "", 日付セル).replace("日", "")
            if len(日付) == 6:
                日付 = "20" + 日付
            元本 = int(re.sub(r"[^\d]", "", str(行[列マップ["元本"]])) or "0")
            利息 = 0
            if "利息" in 列マップ:
                利息 = int(re.sub(r"[^\d]", "", str(行[列マップ["利息"]])) or "0")
            返済額 = 元本 + 利息
            if "返済額" in 列マップ:
                返済額 = int(re.sub(r"[^\d]", "", str(行[列マップ["返済額"]])) or "0")
            if 元本 > 0:
                結果.append({"日付": 日付, "元本": 元本, "利息": 利息, "返済額": 返済額})
        return 結果

    def _返済予定表検索(self, 日付, 金額):
        # 同月+同額で検索
        for 予定 in self.返済予定表:
            if 予定["返済額"] == 金額 and 予定["日付"][:6] == 日付[:6]:
                return 予定
        # 同額のみで検索
        for 予定 in self.返済予定表:
            if 予定["返済額"] == 金額:
                return 予定
        return None

    # ============================================================
    # 通帳取引処理
    # ============================================================

    def 通帳取引処理(self, 取引一覧: list):
        """
        通帳取引リストを処理して仕訳を生成。
        処理済み取引は自動スキップ（重複排除）。
        """
        for 取引 in 取引一覧:
            方向 = "出金" if 取引.出金額 > 0 else "入金"
            金額 = 取引.出金額 if 取引.出金額 > 0 else 取引.入金額

            # 重複チェック
            if self._処理済みチェック(取引.日付, 金額, 取引.摘要, 取引.口座コード, 方向):
                self.処理統計["スキップ"] += 1
                continue

            エントリ一覧 = self._通帳1件処理(取引)
            if エントリ一覧:
                self.確定仕訳一覧.extend(エントリ一覧)
                self._処理済み登録(取引.日付, 金額, 取引.摘要, 取引.口座コード, 方向)
                self.処理統計["新規処理"] += 1

    def _通帳1件処理(self, 取引: 通帳取引):
        """通帳取引1件を仕訳に変換"""
        正式摘要 = self.ルールブック.摘要変換(取引.摘要)
        方向 = "出金" if 取引.出金額 > 0 else "入金"
        金額 = 取引.出金額 if 取引.出金額 > 0 else 取引.入金額
        口座情報 = {
            "科目コード": 取引.口座コード,
            "科目名": self.マスタ.科目名取得(取引.口座コード),
            "補助コード": "", "補助名": "",
        }

        # STEP1: 入金時の売掛金消込チェック
        if 方向 == "入金" and self.売掛金:
            消込結果 = self._売掛金消込処理(取引, 正式摘要, 口座情報, 金額)
            if 消込結果:
                return 消込結果

        # STEP2: ルールブックから仕訳パターンを検索
        パターン = self.ルールブック.パターン検索(正式摘要, 方向, 取引.口座コード, 金額)

        # STEP3: 源泉税対象チェック
        if self.ルールブック.源泉税対象判定(正式摘要):
            return self._源泉税仕訳生成(取引, 正式摘要, 口座情報, 金額)

        # STEP4: 借入金返済チェック
        借入情報 = self._借入金返済チェック(正式摘要, 金額)
        if 借入情報:
            return self._借入金仕訳生成(取引, 正式摘要, 口座情報, 借入情報)

        # STEP5: 通常パターン一致
        if パターン:
            相手科目情報 = {
                "科目コード": パターン["科目コード"],
                "科目名": パターン.get("科目名", self.マスタ.科目名取得(パターン["科目コード"])),
                "補助コード": パターン.get("補助コード", ""),
                "補助名": パターン.get("補助名", self.マスタ.補助科目名取得(
                    パターン["科目コード"], パターン.get("補助コード", ""))),
                "消費税": パターン.get("消費税", {}),
            }
            if 方向 == "出金":
                エントリ = 仕訳エントリ.通帳取引から生成(取引, 借方情報=相手科目情報, 貸方情報=口座情報, 摘要=正式摘要)
            else:
                エントリ = 仕訳エントリ.通帳取引から生成(取引, 借方情報=口座情報, 貸方情報=相手科目情報, 摘要=正式摘要)
            エントリ.信頼度 = "高"
            return [エントリ]

        # STEP6: 不一致 → 174仮払金
        self.要確認一覧.append({
            "日付": 取引.日付, "摘要": 正式摘要, "金額": 金額,
            "方向": 方向, "理由": "ルールブックに一致するパターンなし",
        })
        仮払情報 = {"科目コード": "174", "科目名": "仮払金", "補助コード": "", "補助名": ""}
        if 方向 == "出金":
            エントリ = 仕訳エントリ.通帳取引から生成(取引, 借方情報=仮払情報, 貸方情報=口座情報, 摘要=正式摘要)
        else:
            エントリ = 仕訳エントリ.通帳取引から生成(取引, 借方情報=口座情報, 貸方情報=仮払情報, 摘要=正式摘要)
        エントリ.信頼度 = "低"
        return [エントリ]

    # ============================================================
    # 売掛金消込
    # ============================================================

    def _売掛金消込処理(self, 取引, 摘要, 口座情報, 入金額):
        """入金時の売掛金消込を試行。消込できればエントリを返す、できなければNone"""
        突合結果 = self.売掛金.入金突合(入金額, 摘要)

        if 突合結果["種別"] == "完全一致":
            self.売掛金.消込実行(突合結果["売掛金"])
            エントリ = 仕訳エントリ()
            エントリ.日付 = 取引.日付
            エントリ.借方科目コード = 口座情報["科目コード"]
            エントリ.借方科目名 = 口座情報["科目名"]
            エントリ.借方金額 = 入金額
            エントリ.貸方科目コード, エントリ.貸方科目名 = "150", "売掛金"
            エントリ.貸方金額 = 入金額
            エントリ.摘要 = 突合結果["メモ"]
            エントリ.信頼度 = "高"
            return [エントリ]

        elif 突合結果["種別"] == "手数料差引":
            self.売掛金.消込実行(突合結果["売掛金"])
            請求額 = 突合結果["売掛金"]["請求額"]
            手数料 = 突合結果["手数料"]
            メモ = 突合結果["メモ"]
            結果 = []

            # 借方: 普通預金（入金額）
            e1 = 仕訳エントリ()
            e1.日付 = 取引.日付
            e1.借方科目コード = 口座情報["科目コード"]
            e1.借方科目名 = 口座情報["科目名"]
            e1.借方金額 = 入金額
            e1.貸方科目コード, e1.貸方科目名 = "997", "諸口"
            e1.貸方金額 = 入金額
            e1.摘要, e1.信頼度 = メモ, "高"
            結果.append(e1)

            # 借方: 支払手数料
            e2 = 仕訳エントリ()
            e2.日付 = 取引.日付
            e2.借方科目コード, e2.借方科目名 = "830", "支払手数料"
            e2.借方金額 = 手数料
            e2.貸方科目コード, e2.貸方科目名 = "997", "諸口"
            e2.貸方金額 = 手数料
            e2.摘要 = f"振込手数料 {突合結果['売掛金']['取引先名']}"
            e2.信頼度 = "高"
            結果.append(e2)

            # 貸方: 売掛金（請求額）
            e3 = 仕訳エントリ()
            e3.日付 = 取引.日付
            e3.借方科目コード, e3.借方科目名 = "997", "諸口"
            e3.借方金額 = 請求額
            e3.貸方科目コード, e3.貸方科目名 = "150", "売掛金"
            e3.貸方金額 = 請求額
            e3.摘要, e3.信頼度 = メモ, "高"
            結果.append(e3)

            return 結果

        # 不一致の場合はNoneを返し、通常処理に進む
        return None

    # ============================================================
    # 源泉税仕訳
    # ============================================================

    def _源泉税仕訳生成(self, 取引, 摘要, 口座情報, 金額):
        """源泉税対象の諸口仕訳を生成 + 士業報酬CSV蓄積"""
        結果 = []

        # 逆算: 支払額 ÷ 0.9979 = 税抜報酬額
        税抜額 = math.floor(金額 / 0.9979)
        消費税額 = math.floor(税抜額 * 0.1)
        源泉税額 = math.floor(税抜額 * 0.1021)
        税込総額 = 税抜額 + 消費税額

        if 税込総額 - 源泉税額 != 金額:
            税込総額 = 金額 + 源泉税額

        メモ = f"士業報酬 {摘要} 報酬{税抜額:,}+税{消費税額:,}-源泉{源泉税額:,}"

        # ルールブックから報酬科目を特定
        報酬科目 = self.ルールブック.パターン検索(摘要, "出金", 取引.口座コード, 金額)

        # 借方: 報酬科目（諸口）
        e1 = 仕訳エントリ()
        e1.日付 = 取引.日付
        if 報酬科目:
            e1.借方科目コード = 報酬科目["科目コード"]
            e1.借方科目名 = 報酬科目.get("科目名", self.マスタ.科目名取得(報酬科目["科目コード"]))
        else:
            e1.借方科目コード, e1.借方科目名 = "997", "諸口"
        e1.借方金額 = 税込総額
        e1.貸方科目コード, e1.貸方科目名 = "997", "諸口"
        e1.貸方金額 = 税込総額
        e1.摘要, e1.信頼度 = メモ, "中"
        結果.append(e1)

        # 貸方: 預り金（源泉所得税）
        e2 = 仕訳エントリ()
        e2.日付 = 取引.日付
        e2.借方科目コード, e2.借方科目名 = "997", "諸口"
        e2.借方金額 = 源泉税額
        e2.貸方科目コード, e2.貸方科目名 = "323", "預り金"
        e2.貸方補助コード, e2.貸方補助名 = "1", "源泉所得税"
        e2.貸方金額 = 源泉税額
        e2.摘要, e2.信頼度 = f"{摘要} 源泉", "中"
        結果.append(e2)

        # 貸方: 銀行口座（実際の支払額）
        e3 = 仕訳エントリ()
        e3.日付 = 取引.日付
        e3.借方科目コード, e3.借方科目名 = "997", "諸口"
        e3.借方金額 = 金額
        e3.貸方科目コード = 口座情報["科目コード"]
        e3.貸方科目名 = 口座情報["科目名"]
        e3.貸方金額 = 金額
        e3.摘要, e3.信頼度 = メモ, "中"
        結果.append(e3)

        # 士業報酬CSVに蓄積
        self.士業報酬一覧.append({
            "日付": 取引.日付,
            "士業名": 摘要,
            "報酬額（税抜）": 税抜額,
            "消費税額": 消費税額,
            "源泉税額": 源泉税額,
            "支払額": 金額,
            "摘要": メモ,
        })

        self.要確認一覧.append({
            "日付": 取引.日付, "摘要": メモ, "金額": 金額,
            "方向": "出金", "理由": "源泉税対象（諸口仕訳を自動生成。報酬科目・金額の確認が必要）",
        })

        return 結果

    # ============================================================
    # 借入金返済
    # ============================================================

    def _借入金返済チェック(self, 摘要, 金額):
        for lp in self.ルールブック.借入金パターン.values():
            if not lp.get("摘要集計"):
                continue
            代表摘要 = max(lp["摘要集計"], key=lp["摘要集計"].get)
            if 代表摘要 in 摘要 or 摘要 in 代表摘要:
                return lp
            if 金額 in lp["金額一覧"] or (lp["金額一覧"] and abs(金額 - lp["金額一覧"][-1]) < 100):
                return lp
        return None

    def _借入金仕訳生成(self, 取引, 摘要, 口座情報, 借入情報):
        """借入金返済の仕訳を生成。返済予定表があれば元本・利息を自動分割"""
        結果 = []
        返済額 = 取引.出金額

        # 返済予定表から元本・利息を取得
        予定 = self._返済予定表検索(取引.日付, 返済額)
        if 予定:
            元本 = 予定["元本"]
            利息 = 予定["利息"]
            メモ = f"借入金返済 {摘要} 元本{元本:,}+利息{利息:,}"

            # 元本
            e1 = 仕訳エントリ()
            e1.日付 = 取引.日付
            e1.借方科目コード = 借入情報["コード"]
            e1.借方科目名 = 借入情報["科目名"]
            e1.借方補助コード = 借入情報.get("補助コード", "")
            e1.借方補助名 = 借入情報.get("補助名", "")
            e1.借方金額 = 元本
            e1.貸方科目コード, e1.貸方科目名 = "997", "諸口"
            e1.貸方金額 = 元本
            e1.摘要, e1.信頼度 = メモ, "高"
            結果.append(e1)

            # 利息
            if 利息 > 0:
                e2 = 仕訳エントリ()
                e2.日付 = 取引.日付
                e2.借方科目コード, e2.借方科目名 = "920", "支払利息"
                e2.借方金額 = 利息
                e2.貸方科目コード, e2.貸方科目名 = "997", "諸口"
                e2.貸方金額 = 利息
                e2.摘要, e2.信頼度 = メモ, "高"
                結果.append(e2)

            # 貸方: 銀行口座
            e3 = 仕訳エントリ()
            e3.日付 = 取引.日付
            e3.借方科目コード, e3.借方科目名 = "997", "諸口"
            e3.借方金額 = 返済額
            e3.貸方科目コード = 口座情報["科目コード"]
            e3.貸方科目名 = 口座情報["科目名"]
            e3.貸方金額 = 返済額
            e3.摘要, e3.信頼度 = メモ, "高"
            結果.append(e3)
            return 結果

        # 返済予定表がない場合
        e1 = 仕訳エントリ()
        e1.日付 = 取引.日付
        e1.借方科目コード = 借入情報["コード"]
