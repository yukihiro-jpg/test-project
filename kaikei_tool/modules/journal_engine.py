    def 士業報酬CSV出力(self, 顧問先パス, 顧問先名):
        """士業報酬データを蓄積型CSVに出力"""
        if not self.士業報酬一覧:
            return None

        出力パス = os.path.join(顧問先パス, "output", f"士業報酬_{顧問先名}.csv")
        既存データ = []

        if os.path.isfile(出力パス):
            for 文字コード in ["utf-8-sig", "utf-8", "cp932"]:
                try:
                    with open(出力パス, "r", encoding=文字コード) as f:
                        reader = csv_mod.DictReader(f)
                        既存データ = list(reader)
                    break
                except (UnicodeDecodeError, UnicodeError):
                    continue

        os.makedirs(os.path.dirname(出力パス), exist_ok=True)
        with open(出力パス, "w", encoding="utf-8-sig", newline="") as f:
            writer = csv_mod.DictWriter(f, fieldnames=士業報酬CSV列)
            writer.writeheader()
            for 行 in 既存データ:
                writer.writerow(行)
            for 行 in self.士業報酬一覧:
                row = {}
                for col in 士業報酬CSV列:
                    row[col] = 行.get(col, "")
                row["顧問先名"] = 顧問先名
                writer.writerow(row)

        return 出力パス
