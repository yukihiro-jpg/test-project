# 常陽銀行
# ============================================================
def _常陽銀行解析(テキスト, テーブル一覧, 口座コード):
    """
    常陽銀行の通帳PDFを解析

    【想定フォーマット】
    常陽銀行のネットバンキング（アクセスジェイ）からの取引明細PDF。
    列構成: 日付 | 摘要 | お支払金額 | お預り金額 | 残高
    日付形式: YYYY/MM/DD または MM/DD

    【★事務所PCで要調整★】
    実際のPDFを見せて、列の位置やヘッダーの有無を確認してください。
    """
    結果 = []

    # テーブルベース（列: 日付, 摘要, 出金, 入金, 残高）
    if テーブル一覧:
        for テーブル in テーブル一覧:
            for 行 in テーブル:
                if not 行 or len(行) < 4:
                    continue
                # ヘッダー行をスキップ
                if 行[0] and any(語 in str(行[0]) for 語 in ["日付", "年月日", "取引日"]):
                    continue

                日付 = _日付抽出_スラッシュ(行[0])
                if not 日付:
                    continue

                摘要 = str(行[1]).strip() if 行[1] else ""
                出金 = _金額抽出(行[2]) if len(行) > 2 else 0
                入金 = _金額抽出(行[3]) if len(行) > 3 else 0
                残高 = _金額抽出(行[4]) if len(行) > 4 else 0

                if 摘要 and (出金 > 0 or 入金 > 0):
                    結果.append(通帳取引(
                        日付=日付, 摘要=摘要,
                        出金額=出金, 入金額=入金, 残高=残高,
                        口座コード=口座コード, 元テキスト=str(行),
                    ))

    # テーブル取得できない場合はテキストベース
    if not 結果:
        結果 = _テキストベース解析(テキスト, 口座コード)

    return 結果


