def 銀行名判定(ファイル名, PDFテキスト=""):
    """ファイル名やPDF内テキストから銀行名を判定"""
    検索対象 = ファイル名 + " " + PDFテキスト[:500]
    for キーワード, 銀行名 in 銀行キーワード.items():
        if キーワード in 検索対象:
            return 銀行名
    return "不明"


def 銀行別通帳解析(PDFパス, 口座コード=""):
    """
    銀行名を自動判定して、専用の解析関数を呼び出す。
    通帳PDF解析の代わりにこちらを使う。
    """
    import os
    ファイル名 = os.path.basename(PDFパス)

    # 口座コード推定
    if not 口座コード:
        m = re.match(r"^(\d{2,4})", ファイル名)
        if m:
            口座コード = m.group(1)

    # PDFテキスト取得
    テキスト = PDFテキスト抽出(PDFパス)
    テーブル一覧 = PDFテーブル抽出(PDFパス)

    # 銀行判定
    銀行名 = 銀行名判定(ファイル名, テキスト)

    # 銀行別の解析関数を呼び出し
    解析関数 = {
        "常陽銀行": _常陽銀行解析,
        "筑波銀行": _筑波銀行解析,
        "ゆうちょ銀行": _ゆうちょ銀行解析,
        "茨城県信用組合": _茨城県信組解析,
        "水戸信用金庫": _水戸信金解析,
        "足利銀行": _足利銀行解析,
    }

    if 銀行名 in 解析関数:
        取引一覧 = 解析関数[銀行名](テキスト, テーブル一覧, 口座コード)
        if 取引一覧:
            return 取引一覧

    # どの銀行にも該当しない/解析失敗時は汎用パターン
    return _汎用通帳解析(テキスト, テーブル一覧, 口座コード)


