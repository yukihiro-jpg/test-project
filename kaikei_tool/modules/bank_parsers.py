# 汎用パターン（どの銀行にも該当しない場合）
# ============================================================
def _汎用通帳解析(テキスト, テーブル一覧, 口座コード):
    """どの銀行にも該当しない場合の汎用解析"""
    結果 = []

    if テーブル一覧:
        for テーブル in テーブル一覧:
            for 行 in テーブル:
                if not 行 or len(行) < 4:
                    continue

                # 日付を探す
                日付 = None
                for セル in 行:
                    日付 = _日付抽出_和暦対応(セル)
                    if 日付:
                        break
                if not 日付:
                    continue

                # 摘要を探す（日本語テキストを含むセル）
                摘要 = ""
                for セル in 行:
                    if not セル:
                        continue
                    セル文字列 = str(セル).strip()
                    if (re.search(r"[ぁ-んァ-ヶ一-龥]", セル文字列)
                            and not re.match(r"^\d{4}[/\-]", セル文字列)):
                        摘要 = セル文字列
                        break

                # 金額を探す
                金額一覧 = [_金額抽出(セル) for セル in 行 if セル and _金額抽出(セル) > 0]

                if 摘要 and len(金額一覧) >= 1:
                    出金, 入金, 残高 = 0, 0, 0
                    if len(金額一覧) == 1:
                        出金 = 金額一覧[0]
                    elif len(金額一覧) == 2:
                        出金, 残高 = 金額一覧[0], 金額一覧[1]
                    else:
                        出金, 入金, 残高 = 金額一覧[0], 金額一覧[1], 金額一覧[2]

                    結果.append(通帳取引(
                        日付=日付, 摘要=摘要,
                        出金額=出金, 入金額=入金, 残高=残高,
                        口座コード=口座コード, 元テキスト=str(行),
                    ))

    if not 結果:
        結果 = _テキストベース解析(テキスト, 口座コード)

    return 結果


