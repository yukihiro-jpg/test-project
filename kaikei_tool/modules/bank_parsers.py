def 銀行名判定(ファイル名, PDFテキスト=""):
    """ファイル名やPDF内テキストから銀行名を判定"""
    検索対象 = ファイル名 + " " + PDFテキスト[:500]
    for キーワード, 銀行名 in 銀行キーワード.items():
        if キーワード in 検索対象:
            return 銀行名
    return "不明"


def 銀行別通帳解析(ファイルパス, 口座コード=""):
    """
    CSV/PDFを自動判定して、適切な解析関数を呼び出す。

    CSVファイルの場合: 銀行CSVパーサーで100%精度の解析
    PDFファイルの場合: 従来の銀行別PDFパーサーにフォールバック

    引数:
        ファイルパス: CSV or PDFファイルパス
        口座コード: 口座の科目コード（例: "131"）。省略時はファイル名から推定
    """
    import os
    ファイル名 = os.path.basename(ファイルパス)

    # 口座コード推定（ファイル名先頭の数字）
    if not 口座コード:
        m = re.match(r"^(\d{2,4})", ファイル名)
        if m:
            口座コード = m.group(1)

    # CSV/PDF自動判定
    拡張子 = os.path.splitext(ファイル名)[1].lower()
    if 拡張子 == ".csv":
        return _銀行CSV解析(ファイルパス, ファイル名, 口座コード)
    else:
        return _銀行PDF解析(ファイルパス, ファイル名, 口座コード)


def _銀行CSV解析(CSVパス, ファイル名, 口座コード):
    """
    銀行CSVファイルを解析して取引リストを返す。
    ヘッダー行から列位置を自動推定する。
    """
    # CSV読み込み（文字コード自動判定）
    行一覧 = []
    ヘッダー行 = []
    for 文字コード in ["cp932", "utf-8-sig", "utf-8"]:
        try:
            with open(CSVパス, "r", encoding=文字コード) as f:
                全行 = list(csv_mod.reader(f))
            if 全行:
                行一覧 = 全行
                break
        except (UnicodeDecodeError, UnicodeError):
            continue

    if not 行一覧:
        return []

    # ヘッダー行を探す（日付系キーワードを含む行）
    日付キーワード = ["日付", "取引日", "年月日", "取扱日", "お取引日", "DATE"]
    ヘッダー位置 = 0
    for i, 行 in enumerate(行一覧):
        行文字列 = "".join(str(c) for c in 行)
        if any(語 in 行文字列 for 語 in 日付キーワード):
            ヘッダー行 = 行
            ヘッダー位置 = i
            break

    if not ヘッダー行:
        # ヘッダーが見つからなければ1行目をヘッダーとみなす
        ヘッダー行 = 行一覧[0]
        ヘッダー位置 = 0

    データ行 = 行一覧[ヘッダー位置 + 1:]

    # 銀行名判定 → 列マッピング取得
    銀行名 = 銀行名判定(ファイル名, "")
    if 銀行名 != "不明" and 銀行名 in 銀行CSV列マッピング:
        列マップ = _CSV列位置特定(ヘッダー行, 銀行CSV列マッピング[銀行名])
    else:
        列マップ = _CSV列位置特定(ヘッダー行, 汎用CSV列キーワード)

    if "日付" not in 列マップ:
        return []

    # 取引データを抽出
    結果 = []
    for 行 in データ行:
        if not 行 or len(行) <= max(列マップ.values(), default=0):
            continue

        # 日付
        日付セル = 行[列マップ["日付"]].strip() if "日付" in 列マップ else ""
        日付 = _日付抽出_和暦対応(日付セル)
        if not 日付:
            continue

        # 摘要
        摘要 = 行[列マップ["摘要"]].strip() if "摘要" in 列マップ else ""

        # 金額
        出金 = _CSV金額抽出(行, 列マップ.get("出金"))
        入金 = _CSV金額抽出(行, 列マップ.get("入金"))
        残高 = _CSV金額抽出(行, 列マップ.get("残高"))

        if 摘要 and (出金 > 0 or 入金 > 0):
            結果.append(通帳取引(
                日付=日付, 摘要=摘要,
                出金額=出金, 入金額=入金, 残高=残高,
                口座コード=口座コード, 元テキスト=str(行),
            ))

    return 結果


def _銀行PDF解析(PDFパス, ファイル名, 口座コード):
    """従来の銀行別PDF解析（フォールバック用）"""
    テキスト = PDFテキスト抽出(PDFパス)
    テーブル一覧 = PDFテーブル抽出(PDFパス)

    銀行名 = 銀行名判定(ファイル名, テキスト)

    解析関数 = {
        "常陽銀行": _常陽銀行解析,
        "筑波銀行": _筑波銀行解析,
        "ゆうちょ銀行": _ゆうちょ銀行解析,
        "茨城県信用組合": _茨城県信組解析,
        "水戸信用金庫": _水戸信金解析,
        "足利銀行": _足利銀行解析,
    }

    if 銀行名 in 解析関数:
        取引一覧 = 解析関数[銀行名](テキスト, テーブル一覧, 口座コード)
        if 取引一覧:
            return 取引一覧

    return _汎用通帳解析(テキスト, テーブル一覧, 口座コード)


def _CSV列位置特定(ヘッダー行, 列定義):
    """
    CSVヘッダー行から各フィールドの列位置を特定する

    引数:
        ヘッダー行: CSVのヘッダー行（リスト）
        列定義: {"フィールド名": ["キーワード1", "キーワード2", ...]} の辞書

    戻り値:
        {"フィールド名": 列番号} の辞書
    """
    列マップ = {}
    ヘッダー正規化 = [re.sub(r"\s+", "", str(c)) for c in ヘッダー行]

    for フィールド名, キーワード一覧 in 列定義.items():
        for キーワード in キーワード一覧:
            正規化キーワード = re.sub(r"\s+", "", キーワード)
            for i, ヘッダー in enumerate(ヘッダー正規化):
                if 正規化キーワード in ヘッダー:
                    列マップ[フィールド名] = i
                    break
            if フィールド名 in 列マップ:
                break

    return 列マップ


def _CSV金額抽出(行, 列番号):
    """CSV行から金額を抽出（列番号がNoneなら0）"""
    if 列番号 is None or 列番号 >= len(行):
        return 0
    テキスト = str(行[列番号]).strip()
    if not テキスト:
        return 0
    数字のみ = re.sub(r"[^\d]", "", テキスト)
    return int(数字のみ) if 数字のみ else 0


