def _日付抽出_スラッシュ(セル):
    """YYYY/MM/DD または MM/DD 形式の日付を抽出"""
    if not セル:
        return None
    m = re.search(r"(\d{4})[/\-](\d{1,2})[/\-](\d{1,2})", str(セル))
    if m:
        return f"{m.group(1)}{int(m.group(2)):02d}{int(m.group(3)):02d}"
    m = re.search(r"(\d{1,2})[/\-](\d{1,2})", str(セル))
    if m:
        return _日付正規化(m.group(0))
    return None


def _日付抽出_和暦対応(セル):
    """YYYY/MM/DD、令和X年X月X日、R7.3.20 等に対応"""
    if not セル:
        return None
    セル文字列 = str(セル)

    # 西暦 YYYY/MM/DD
    m = re.search(r"(\d{4})[/\-](\d{1,2})[/\-](\d{1,2})", セル文字列)
    if m:
        return f"{m.group(1)}{int(m.group(2)):02d}{int(m.group(3)):02d}"

    # 令和X年X月X日
    m = re.search(r"令和\s*(\d+)\s*年\s*(\d{1,2})\s*月\s*(\d{1,2})\s*日", セル文字列)
    if m:
        年 = int(m.group(1)) + 2018
        return f"{年}{int(m.group(2)):02d}{int(m.group(3)):02d}"

    # R7/3/20 or R7.3.20
    m = re.search(r"R\s*(\d+)\s*[/.]\s*(\d{1,2})\s*[/.]\s*(\d{1,2})", セル文字列)
    if m:
        年 = int(m.group(1)) + 2018
        return f"{年}{int(m.group(2)):02d}{int(m.group(3)):02d}"

    # MM/DD（年なし）
    m = re.search(r"(\d{1,2})[/\-](\d{1,2})", セル文字列)
    if m:
        return _日付正規化(m.group(0))

    return None


def _テキストベース解析(テキスト, 口座コード):
    """テキストから取引データを抽出する汎用パターン"""
    結果 = []
    for 行 in テキスト.split("\n"):
        行 = 行.strip()
        if not 行:
            continue

        日付 = _日付抽出_和暦対応(行)
        if not 日付:
            continue

        # 金額検出
        金額一覧 = re.findall(r"[\d,]+\d", 行)
        金額一覧 = [int(a.replace(",", "")) for a in 金額一覧 if int(a.replace(",", "")) > 0]
        if not 金額一覧:
            continue

        # 日付と金額の間の部分が摘要
        日付一致 = re.search(
            r"(\d{4}[/\-]\d{1,2}[/\-]\d{1,2}|令和\s*\d+\s*年.*?日|R\s*\d+[/.]\d+[/.]\d+|\d{1,2}[/\-]\d{1,2})",
            行
        )
        if 日付一致:
            残り = 行[日付一致.end():].strip()
            金額一致 = re.search(r"[\d,]{2,}", 残り)
            摘要 = 残り[:金額一致.start()].strip() if 金額一致 else 残り
        else:
            摘要 = ""

        if not 摘要:
            continue

        出金, 入金, 残高 = 0, 0, 0
        if len(金額一覧) == 1:
            出金 = 金額一覧[0]
        elif len(金額一覧) == 2:
            出金, 残高 = 金額一覧[0], 金額一覧[1]
        elif len(金額一覧) >= 3:
            出金, 入金, 残高 = 金額一覧[0], 金額一覧[1], 金額一覧[2]

        結果.append(通帳取引(
            日付=日付, 摘要=摘要,
            出金額=出金, 入金額=入金, 残高=残高,
            口座コード=口座コード, 元テキスト=行,
        ))

    return 結果
