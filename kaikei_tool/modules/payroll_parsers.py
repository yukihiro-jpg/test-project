"""
賃金台帳CSVパーサー

マネーフォワード給与、弥生給与、楽しい給与計算、汎用CSVに対応。
ヘッダー行から列位置を自動推定する。
"""
import csv
import os
import re

from config import 給与CSV列マッピング, 汎用給与CSV列キーワード
from .pdf_reader import 賃金台帳データ


def 賃金台帳CSV解析(CSVパス):
    """
    賃金台帳CSVを解析して賃金データリストを返す。
    給与ソフトを自動判定し、適切なパーサーで処理する。

    引数:
        CSVパス: CSVファイルパス

    戻り値:
        list[賃金台帳データ]
    """
    行一覧, ヘッダー行 = _CSV読み込み(CSVパス)
    if not 行一覧 or not ヘッダー行:
        return []

    # 給与ソフトを自動判定
    ソフト名 = _給与ソフト判定(ヘッダー行, CSVパス)

    # ヘッダーから列位置をマッピング
    if ソフト名 and ソフト名 in 給与CSV列マッピング:
        列マップ = _列位置特定(ヘッダー行, 給与CSV列マッピング[ソフト名])
    else:
        列マップ = _列位置特定(ヘッダー行, 汎用給与CSV列キーワード)

    if not 列マップ.get("従業員名") and not 列マップ.get("支給合計"):
        return []

    結果 = []
    for 行 in 行一覧:
        if len(行) <= max(列マップ.values(), default=0):
            continue

        データ = 賃金台帳データ()

        # 各フィールドを読み取り
        if "従業員名" in 列マップ:
            データ.従業員名 = 行[列マップ["従業員名"]].strip()
        if not データ.従業員名:
            continue  # 従業員名がなければスキップ

        データ.基本給 = _金額変換(行, 列マップ.get("基本給"))
        データ.残業代 = _金額変換(行, 列マップ.get("残業代"))
        データ.通勤手当 = _金額変換(行, 列マップ.get("通勤手当"))
        データ.支給合計 = _金額変換(行, 列マップ.get("支給合計"))
        データ.健康保険 = _金額変換(行, 列マップ.get("健康保険"))
        データ.厚生年金 = _金額変換(行, 列マップ.get("厚生年金"))
        データ.雇用保険 = _金額変換(行, 列マップ.get("雇用保険"))
        データ.源泉所得税 = _金額変換(行, 列マップ.get("源泉所得税"))
        データ.住民税 = _金額変換(行, 列マップ.get("住民税"))
        データ.控除合計 = _金額変換(行, 列マップ.get("控除合計"))
        データ.差引支給額 = _金額変換(行, 列マップ.get("差引支給額"))

        # 支給合計が未設定なら基本給+残業代+通勤手当から計算
        if データ.支給合計 == 0 and データ.基本給 > 0:
            データ.支給合計 = データ.基本給 + データ.残業代 + データ.通勤手当

        # 控除合計が未設定なら各控除から計算
        if データ.控除合計 == 0:
            データ.控除合計 = (データ.健康保険 + データ.厚生年金 + データ.雇用保険
                           + データ.源泉所得税 + データ.住民税)

        # 差引支給額が未設定なら計算
        if データ.差引支給額 == 0 and データ.支給合計 > 0:
            データ.差引支給額 = データ.支給合計 - データ.控除合計

        データ.元テキスト = str(行)

        if データ.支給合計 > 0:
            結果.append(データ)

    return 結果


def _CSV読み込み(CSVパス):
    """CSVを読み込み、ヘッダー行とデータ行を返す"""
    for 文字コード in ["cp932", "utf-8-sig", "utf-8"]:
        try:
            with open(CSVパス, "r", encoding=文字コード) as f:
                全行 = list(csv.reader(f))
            if not 全行:
                continue

            # ヘッダー行を探す（「従業員」「氏名」「基本給」等を含む行）
            ヘッダー語 = ["従業員", "氏名", "名前", "社員", "基本給", "支給"]
            for i, 行 in enumerate(全行):
                行文字列 = "".join(str(c) for c in 行)
                if any(語 in 行文字列 for 語 in ヘッダー語):
                    return 全行[i + 1:], 行
            # ヘッダーが見つからなければ1行目をヘッダーとみなす
            return 全行[1:], 全行[0]
        except (UnicodeDecodeError, UnicodeError):
            continue
    return [], []


def _給与ソフト判定(ヘッダー行, CSVパス):
    """ヘッダー内容やファイル名から給与ソフトを判定"""
    ファイル名 = os.path.basename(CSVパス)
    ヘッダー文字列 = "".join(str(c) for c in ヘッダー行)

    if "マネーフォワード" in ファイル名 or "moneyforward" in ファイル名.lower():
        return "マネーフォワード"
    if "弥生" in ファイル名 or "yayoi" in ファイル名.lower():
        return "弥生給与"
    if "楽しい" in ファイル名:
        return "楽しい給与計算"

    # ヘッダー内容から推定
    if "支給額合計" in ヘッダー文字列 or "控除額合計" in ヘッダー文字列:
        return "マネーフォワード"
    if "銀行振込額" in ヘッダー文字列:
        return "弥生給与"
    if "手取り" in ヘッダー文字列 or "支給総額" in ヘッダー文字列:
        return "楽しい給与計算"

    return None


def _列位置特定(ヘッダー行, 列定義):
    """
    ヘッダー行から各フィールドの列位置を特定する

    引数:
        ヘッダー行: CSVのヘッダー行（リスト）
        列定義: {"フィールド名": ["キーワード1", "キーワード2", ...]} の辞書

    戻り値:
        {"フィールド名": 列番号} の辞書
    """
    列マップ = {}
    ヘッダー正規化 = [re.sub(r"\s+", "", str(c)) for c in ヘッダー行]

    for フィールド名, キーワード一覧 in 列定義.items():
        for キーワード in キーワード一覧:
            正規化キーワード = re.sub(r"\s+", "", キーワード)
            for i, ヘッダー in enumerate(ヘッダー正規化):
                if 正規化キーワード in ヘッダー:
                    列マップ[フィールド名] = i
                    break
            if フィールド名 in 列マップ:
                break

    return 列マップ


def _金額変換(行, 列番号):
    """列番号が指定されていれば金額を抽出、なければ0"""
    if 列番号 is None or 列番号 >= len(行):
        return 0
    テキスト = str(行[列番号]).strip()
    if not テキスト:
        return 0
    数字のみ = re.sub(r"[^\d]", "", テキスト)
    return int(数字のみ) if 数字のみ else 0
