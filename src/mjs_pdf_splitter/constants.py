"""書類種類パターン・会社名パターン・決算期パターンの定義"""

import re

# ========================================================================
# 書類種類の判定パターン
# ========================================================================
# 各ページのテキスト（空白除去済み）に対してマッチさせる。
# 優先度順（先にマッチしたものが採用される）。
#
# 【判定ロジック】
# 1. 様式番号（第六号様式等）を最優先で判定
#    → 県税・市税・償却資産の各申告書に固有の番号
# 2. 「別表」があれば法人税申告書
#    → 別表ページには道府県民税・消費税等のキーワードが出現するが、
#      別表であれば確実に法人税の構成要素
# 3. その他のキーワードで判定

DOCUMENT_TYPE_PATTERNS: list[tuple[re.Pattern, str]] = [
    # === 最優先: 様式番号（各申告書に固有の識別子） ===
    # 漢数字とアラビア数字の両方に対応
    (re.compile(r"第(六|6|６)号様式"), "県税申告書"),
    (re.compile(r"第(二十|20|２０)号様式"), "市税申告書"),
    (re.compile(r"第(二十六|26|２６)号様式"), "償却資産税申告書"),

    # === 次優先: 別表 → 法人税申告書 ===
    # 様式番号が見つからない場合、「別表」は法人税申告書の構成要素。
    # 別表ページに「道府県民税」「消費税」「減価償却資産」等が
    # 本文中に出現しても、法人税申告書として正しく判定される。
    (re.compile(r"別表"), "法人税申告書"),

    # === 特定書類（一意性が高い） ===
    (re.compile(r"税務代理権限証書"), "税務代理権限証書"),
    (re.compile(r"事業概況説明書"), "法人事業概況説明書"),

    # === 決算報告書（財務諸表のキーワード） ===
    (re.compile(
        r"(貸借対照表|損益計算書|株主資本等変動計算書"
        r"|社員資本等変動計算書|個別注記表)"
    ), "決算報告書"),

    # === 勘定科目内訳明細書 ===
    # MJSでは「〇〇の内訳書」という形式で各ページに記載される
    # 「勘定科目内訳明細書」自体は「内訳書」を含まないため両方記載
    (re.compile(r"(内訳書|内訳明細書)"), "勘定科目内訳明細書"),

    # === 適用額明細書 ===
    # 別表一のチェック欄「適用額明細書 提出の有無」に誤マッチしないよう
    # 直後に「提出」が続く場合は除外する
    (re.compile(r"適用額明細書(?!提出)"), "適用額明細書"),

    # === 償却資産税申告書（様式番号がない場合のフォールバック） ===
    # 別表十六の「減価償却資産」に誤マッチしないよう負の後読みを使用
    (re.compile(r"(?<!減価)償却資産"), "償却資産税申告書"),

    # === 県税申告書（様式番号がない場合のフォールバック） ===
    (re.compile(r"(道府県民税|都民税)"), "県税申告書"),

    # === 市税申告書（様式番号がない場合のフォールバック） ===
    (re.compile(r"(市町?村?民税|特別区民税)"), "市税申告書"),

    # === 消費税申告書 ===
    (re.compile(r"消費税"), "消費税申告書"),

    # === 法人税（別表以外の法人税関連ページ） ===
    (re.compile(r"法人税"), "法人税申告書"),
]

# ========================================================================
# 会社名（法人名）抽出パターン
# ========================================================================
LEGAL_ENTITY_TYPES = [
    "株式会社", "有限会社", "合同会社", "合資会社", "合名会社",
    "一般社団法人", "一般財団法人", "公益社団法人", "公益財団法人",
    "特定非営利活動法人", "医療法人", "社会福祉法人",
    "学校法人", "宗教法人", "協同組合",
]

# 法人格が前に来るパターン（例: 合同会社和泉、株式会社サンプル）
_entity_prefix = (
    r"(?:" + "|".join(LEGAL_ENTITY_TYPES) + r")"
    r"[\u3000-\u9FFF\uF900-\uFAFF\w]+"
)
# 法人格が後に来るパターン（例: サンプル株式会社）
_entity_suffix = (
    r"[\u3000-\u9FFF\uF900-\uFAFF\w]+"
    r"(?:" + "|".join(LEGAL_ENTITY_TYPES) + r")"
)

# 法人名は空白・改行・文頭の直後に出現する前提
COMPANY_NAME_PATTERN = re.compile(
    r"(?:^|(?<=[\s\n　]))"
    r"(" + _entity_prefix + r"|" + _entity_suffix + r")",
    re.MULTILINE,
)

# ========================================================================
# 決算期（和暦）抽出パターン
# ========================================================================
FISCAL_PERIOD_PATTERN = re.compile(
    r"(令和|平成|昭和)\s*(\d{1,2})\s*年\s*(\d{1,2})\s*月"
)

# ========================================================================
# ファイル名サニタイズ
# ========================================================================
INVALID_FILENAME_CHARS = re.compile(r'[\\/:*?"<>|\n\r\t]')
