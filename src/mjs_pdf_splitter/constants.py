"""書類種類パターン・会社名パターン・決算期パターンの定義"""

import re

# ========================================================================
# 書類種類の判定パターン
# ========================================================================
# 各ページの全文テキスト（空白除去済み）に対してマッチさせる。
# 優先度順（先にマッチしたものが採用される）。
# より具体的なパターンを先に、汎用的なパターンを後に配置する。

DOCUMENT_TYPE_PATTERNS: list[tuple[re.Pattern, str]] = [
    # --- 特定書類（一意性が高い） ---
    (re.compile(r"税務代理権限証書"), "税務代理権限証書"),
    (re.compile(r"適用額明細書"), "適用額明細書"),
    (re.compile(r"償却資産"), "償却資産税申告書"),

    # --- 事業概況説明書 ---
    (re.compile(r"事業概況説明書"), "法人事業概況説明書"),

    # --- 決算報告書（財務諸表のキーワード） ---
    (re.compile(
        r"(貸借対照表|損益計算書|株主資本等変動計算書"
        r"|社員資本等変動計算書|個別注記表)"
    ), "決算報告書"),

    # --- 勘定科目内訳明細書 ---
    # MJSでは「〇〇の内訳書」という形式で各ページに記載される
    # 「勘定科目内訳明細書」自体には「内訳書」が部分文字列として含まれないため両方記載
    (re.compile(r"(内訳書|内訳明細書)"), "勘定科目内訳明細書"),

    # --- 県税申告書（道府県民税・事業税） ---
    (re.compile(r"(道府県民税|都民税|第六号様式)"), "県税申告書"),

    # --- 市税申告書（市町村民税） ---
    (re.compile(r"(市町?村?民税|特別区民税|第二十号様式)"), "市税申告書"),

    # --- 消費税申告書 ---
    (re.compile(r"消費税"), "消費税申告書"),

    # --- 法人税申告書（別表 = 法人税の付属書類） ---
    # 「別表」はほぼ法人税申告書専用のキーワード。最も汎用的なので最後に配置。
    (re.compile(r"(別表|法人税)"), "法人税申告書"),
]

# ========================================================================
# 会社名（法人名）抽出パターン
# ========================================================================
# MJSの税務書類では法人格（株式会社、合同会社等）が必ず含まれるため、
# 法人格パターンで抽出する方が、フォームラベルの誤検出を防げる。

LEGAL_ENTITY_TYPES = [
    "株式会社", "有限会社", "合同会社", "合資会社", "合名会社",
    "一般社団法人", "一般財団法人", "公益社団法人", "公益財団法人",
    "特定非営利活動法人", "医療法人", "社会福祉法人",
    "学校法人", "宗教法人", "協同組合",
]

# 法人格が前に来るパターン（例: 合同会社和泉、株式会社サンプル）
_entity_prefix = (
    r"(?:" + "|".join(LEGAL_ENTITY_TYPES) + r")"
    r"[\u3000-\u9FFF\uF900-\uFAFF\w]+"
)
# 法人格が後に来るパターン（例: サンプル株式会社）
_entity_suffix = (
    r"[\u3000-\u9FFF\uF900-\uFAFF\w]+"
    r"(?:" + "|".join(LEGAL_ENTITY_TYPES) + r")"
)

# 法人名は空白・改行・文頭の直後に出現する前提
# （「別表一合同会社」のような誤マッチを防ぐ）
COMPANY_NAME_PATTERN = re.compile(
    r"(?:^|(?<=[\s\n　]))"
    r"(" + _entity_prefix + r"|" + _entity_suffix + r")",
    re.MULTILINE,
)

# ========================================================================
# 決算期（和暦）抽出パターン
# ========================================================================
FISCAL_PERIOD_PATTERN = re.compile(
    r"(令和|平成|昭和)\s*(\d{1,2})\s*年\s*(\d{1,2})\s*月"
)

# ========================================================================
# ファイル名サニタイズ
# ========================================================================
INVALID_FILENAME_CHARS = re.compile(r'[\\/:*?"<>|\n\r\t]')
