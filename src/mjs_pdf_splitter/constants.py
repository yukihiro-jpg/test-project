"""書類種類パターン・会社名パターン・決算期パターン・ナンバリングの定義"""

import re

# ========================================================================
# ナンバリングマップ
# ========================================================================
# 書類種類 → ファイル名先頭の番号
# 該当しない書類はナンバリングなし
DOCUMENT_NUMBER_MAP: dict[str, str] = {
    "法人税申告書": "01",
    "決算報告書": "02",
    "勘定科目内訳明細書": "03",
    "法人事業概況説明書": "04",
    "税務代理権限証書": "05",
    "県税申告書": "06",
    "市税申告書": "07",
    "消費税申告書（原則）": "08",
    "消費税申告書（簡易）": "08",
    "メール詳細": "09",
    "ダイレクト納付情報": "10",
}

# ========================================================================
# 書類種類の判定パターン
# ========================================================================
# 優先度順（先にマッチしたものが採用される）。
#
# 【判定ロジック】
# 1. 様式番号（第六号様式等）を最優先で判定
# 2. 「別表」があれば法人税申告書
# 3. その他のキーワードで判定

DOCUMENT_TYPE_PATTERNS: list[tuple[re.Pattern, str]] = [
    # === 最優先: 様式番号 ===
    (re.compile(r"第(六|6|６)号様式"), "県税申告書"),
    (re.compile(r"第(二十|20|２０)号様式"), "市税申告書"),
    (re.compile(r"第(二十六|26|２６)号様式"), "償却資産税申告書"),

    # === 次優先: 別表 → 法人税申告書 ===
    (re.compile(r"別表"), "法人税申告書"),

    # === 特定書類 ===
    (re.compile(r"税務代理権限証書"), "税務代理権限証書"),
    (re.compile(r"事業概況説明書"), "法人事業概況説明書"),

    # === ダイレクト納付情報（メール詳細より先に判定） ===
    # 「受信通知（納付区分番号通知）」はダイレクト納付情報に分類するため
    # メール詳細より先にチェックする
    (re.compile(r"(納付確認|納付区分番号通知|ダイレクト納付指定日)"), "ダイレクト納付情報"),

    # === メール詳細（受信通知・申告受付完了通知） ===
    (re.compile(r"(受信通知|申告受付完了通知)"), "メール詳細"),

    # === 決算報告書 ===
    (re.compile(
        r"(貸借対照表|損益計算書|株主資本等変動計算書"
        r"|社員資本等変動計算書|個別注記表)"
    ), "決算報告書"),

    # === 勘定科目内訳明細書 ===
    (re.compile(r"(内訳書|内訳明細書)"), "勘定科目内訳明細書"),

    # === 適用額明細書 ===
    (re.compile(r"適用額明細書(?!提出)"), "適用額明細書"),

    # === 償却資産税申告書（様式番号がない場合のフォールバック） ===
    (re.compile(r"(?<!減価)償却資産"), "償却資産税申告書"),

    # === 県税・市税（様式番号がない場合のフォールバック） ===
    (re.compile(r"(道府県民税|都民税)"), "県税申告書"),
    (re.compile(r"(市町?村?民税|特別区民税)"), "市税申告書"),

    # === 消費税申告書 ===
    # 原則/簡易の判定は classifier.py で後処理する
    (re.compile(r"消費税"), "消費税申告書"),

    # === 法人税（別表以外） ===
    (re.compile(r"法人税"), "法人税申告書"),
]

# ========================================================================
# 消費税の原則/簡易判定パターン
# ========================================================================
SIMPLIFIED_TAX_PATTERN = re.compile(r"簡易課税")

# ========================================================================
# 会社名（法人名）抽出パターン
# ========================================================================
LEGAL_ENTITY_TYPES = [
    "株式会社", "有限会社", "合同会社", "合資会社", "合名会社",
    "一般社団法人", "一般財団法人", "公益社団法人", "公益財団法人",
    "特定非営利活動法人", "医療法人", "社会福祉法人",
    "学校法人", "宗教法人", "協同組合",
]

_entity_prefix = (
    r"(?:" + "|".join(LEGAL_ENTITY_TYPES) + r")"
    r"[\u3000-\u9FFF\uF900-\uFAFF\w]+"
)
_entity_suffix = (
    r"[\u3000-\u9FFF\uF900-\uFAFF\w]+"
    r"(?:" + "|".join(LEGAL_ENTITY_TYPES) + r")"
)

COMPANY_NAME_PATTERN = re.compile(
    r"(?:^|(?<=[\s\n　]))"
    r"(" + _entity_prefix + r"|" + _entity_suffix + r")",
    re.MULTILINE,
)

# ========================================================================
# 決算期（和暦）抽出パターン
# ========================================================================
# 優先: 「至」の後に出現する日付 = 事業年度の終了月
# （設立年月日等の無関係な日付を拾わないようにする）
FISCAL_PERIOD_PATTERN_PREFERRED = re.compile(
    r"至\s*(令和|平成|昭和)\s*(\d{1,2})\s*年\s*(\d{1,2})\s*月"
)
# フォールバック: 「至」がない場合に使う汎用パターン
FISCAL_PERIOD_PATTERN_FALLBACK = re.compile(
    r"(令和|平成|昭和)\s*(\d{1,2})\s*年\s*(\d{1,2})\s*月"
)

# ========================================================================
# ファイル名サニタイズ
# ========================================================================
INVALID_FILENAME_CHARS = re.compile(r'[\\/:*?"<>|\n\r\t]')
