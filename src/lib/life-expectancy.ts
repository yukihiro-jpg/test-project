/**
 * 平均余命を取得する（端数切り捨て）
 * @param age 年齢
 * @param sex 性別
 * @returns 平均余命の整数年数
 */
export function getLifeExpectancyYears(
  age: number,
  sex: 'male' | 'female',
): number {
  const table = sex === 'male' ? MALE_LIFE_EXPECTANCY : FEMALE_LIFE_EXPECTANCY;
  const clampedAge = Math.min(Math.max(0, Math.floor(age)), 100);
  const expectancy = table[clampedAge];
  return Math.floor(expectancy);
}
